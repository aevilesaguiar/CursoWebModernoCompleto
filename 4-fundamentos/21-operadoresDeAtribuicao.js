const a = 7;
let b = 3;

console.log(b+=a); // operação aditiva -> b=b+a;

b-=4;
console.log(b); // operação subtrativa-> b=b-a;

b*=3;

console.log(b); // operação multiplicativa-> b=b*a;

b/=2;

console.log(b); // operação multiplicativa-> b=b/a;

b%=2;

console.log(b); // operação modular-> b=b%2;